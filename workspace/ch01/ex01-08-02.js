/*
다음과 같은 a,b가 주어졌을 때, +, - , *, /, %, ** 산술 연산자를 사용하여 계산된 결과를 출력하시오.

출력결과
13
7
30
3.333333333
1
1000
*/

let a = 10;
let b = 3;

console.log(a + b); // 13
console.log(a - b); // 7
console.log(a * b); // 30
console.log(a / b); // 3.3333333333333335
console.log(a % b); // 1
console.log(a ** b); // 1000
